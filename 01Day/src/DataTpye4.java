
public class DataTpye4 {
	
	public static void main(String[] args) {
		// 3.14 저장
		//실수는 더블로 처리하려고 한다
		//그런데 float으로 처리하고 싶을때 알려줘야 한다 끝에 F붙이기
		//Double로 처리하고 싶을 때 끝에 d붙이거나 안붙여도 디폴트값 
		float f= 3.14F; 
		double f2 = 3.14;
		double f3 = 3.14d;
		double f4 = 3.14D;
		
		boolean x=true;
		boolean x2=false;
		
		System.out.println(f);
		System.out.println(f2);
		System.out.println(f3);
		System.out.println(f4);
		System.out.println(x);
		System.out.println(x2);

	}

}
